"""Measure the cross-GPU noise floor, so ``delta_threshold`` can be set, not guessed.

This is the tool for the subnet's single largest open consensus risk. Leoma crowns a
challenger when its bootstrap lower-confidence-bound beats the king by
``delta_threshold``. That margin only means anything if it is *wider than the noise*:
the same model, generating the same clips from the same seed, produces slightly
different frames on an H100 than on an A100 (14B bf16 diffusion is not bit-exact across
architectures), hence slightly different distances, hence a slightly different verdict.
If two honest validators land on opposite sides of the margin on a near-threshold
challenger, they fork the king chain forever. Today ``delta_threshold`` is a hopeful
constant; this makes it a *measured multiple of a known floor*.

**The experiment.** Generate one real model on the calibration clips on *each* GPU
type in the fleet (:func:`box_record`, run on the box). Then, for every pair of boxes,
the per-clip distance difference ``d_i = dist_A[i] - dist_B[i]`` is exactly the
cross-GPU noise — because the model, the clip, the seed and the truth are all
identical, so *only the hardware differs*. A self-duel between two honest boxes'
generations of the same model has a true mean advantage of **zero**; whatever nonzero
``mean(d)`` the bootstrap finds is pure hardware noise, and its magnitude is how far a
marginal verdict can stray from the truth. :func:`analyze` computes that across all
pairs and recommends a ``delta_threshold`` above it.

The analysis here is pure and unit-tested. :func:`box_record` needs a GPU and reuses
the production generation + scoring path, so it is exercised on real hardware, not in
CI.
"""
from __future__ import annotations

from dataclasses import dataclass
from typing import Optional, Sequence

import numpy as np

from leoma.eval.bootstrap import paired_bootstrap_verdict

#: How far above the measured noise floor to set the margin. The recommendation is
#: this times the worst spurious mean-advantage any box pair produced.
DEFAULT_SAFETY_FACTOR = 3.0


@dataclass(frozen=True)
class PairResult:
    """Cross-GPU noise between one pair of boxes, on the same model + clips."""

    gpu_a: str
    gpu_b: str
    n_clips: int
    bit_identical_clips: int          # clips where both boxes generated byte-identical frames
    abs_delta_mean: float
    abs_delta_p95: float
    abs_delta_p99: float
    abs_delta_max: float
    mu_hat: float                     # mean(dist_A - dist_B); true value is 0, so this is bias
    lcb: float
    ucb: float

    def as_dict(self) -> dict:
        return {
            "gpu_a": self.gpu_a, "gpu_b": self.gpu_b, "n_clips": self.n_clips,
            "bit_identical_clips": self.bit_identical_clips,
            "abs_delta_mean": self.abs_delta_mean, "abs_delta_p95": self.abs_delta_p95,
            "abs_delta_p99": self.abs_delta_p99, "abs_delta_max": self.abs_delta_max,
            "mu_hat": self.mu_hat, "lcb": self.lcb, "ucb": self.ucb,
        }


@dataclass(frozen=True)
class CalibrationReport:
    n_boxes: int
    n_pairs: int
    gpus: tuple[str, ...]
    #: Worst spurious mean-advantage across all pairs — the number delta must beat.
    max_abs_mu_hat: float
    #: Worst single-clip distance disagreement across all pairs.
    max_abs_delta: float
    #: The fraction of box-pairs that were byte-identical (fully reproducible).
    fully_reproducible_pairs: int
    recommended_delta_threshold: float
    current_delta_threshold: Optional[float]
    verdict: str                      # human-readable pass/fail against the current pin
    pairs: tuple[PairResult, ...]

    def as_dict(self) -> dict:
        return {
            "n_boxes": self.n_boxes, "n_pairs": self.n_pairs, "gpus": list(self.gpus),
            "max_abs_mu_hat": self.max_abs_mu_hat, "max_abs_delta": self.max_abs_delta,
            "fully_reproducible_pairs": self.fully_reproducible_pairs,
            "recommended_delta_threshold": self.recommended_delta_threshold,
            "current_delta_threshold": self.current_delta_threshold,
            "verdict": self.verdict,
            "pairs": [p.as_dict() for p in self.pairs],
        }


def _clip_map(record: dict) -> dict[str, dict]:
    return {c["clip_id"]: c for c in record.get("clips", [])}


def _validate_records(records: Sequence[dict]) -> list[str]:
    """Every box must have scored the SAME model on the SAME clips, or the pairwise
    difference is measuring a different exam, not hardware noise."""
    if len(records) < 2:
        raise ValueError("need at least 2 box records to measure cross-GPU noise")

    models = {r.get("model") for r in records}
    if len(models) != 1:
        raise ValueError(f"records are for different models {models}; the calibration model "
                         "must be identical across boxes or the difference is not hardware noise")

    corpora = {r.get("corpus_id") for r in records}
    if len(corpora) != 1:
        raise ValueError(f"records are for different corpora {corpora}")

    clip_sets = [frozenset(_clip_map(r)) for r in records]
    common = set.intersection(*(set(s) for s in clip_sets))
    if not common:
        raise ValueError("box records share no common clip_ids")
    return sorted(common)


def _pair(a: dict, b: dict, clip_ids: Sequence[str], *, alpha: float, n_bootstrap: int, seed: int) -> PairResult:
    am, bm = _clip_map(a), _clip_map(b)
    da = np.array([am[c]["distance"] for c in clip_ids], dtype=np.float64)
    db = np.array([bm[c]["distance"] for c in clip_ids], dtype=np.float64)
    delta = da - db
    abs_delta = np.abs(delta)

    bit_identical = sum(
        1 for c in clip_ids
        if am[c].get("frames_digest") and am[c].get("frames_digest") == bm[c].get("frames_digest")
    )

    # A self-duel between the two boxes' generations: true advantage is 0, so mu_hat is
    # bias and [lcb, ucb] brackets how far a marginal verdict can drift. Reuse the exact
    # production bootstrap so the number means what the duel means.
    v = paired_bootstrap_verdict(da, db, delta_threshold=0.0, alpha=alpha,
                                 n_bootstrap=n_bootstrap, seed=seed)
    # The two-sided concern: symmetric bound on |mean advantage|.
    ucb = float(np.quantile(_bootstrap_means(delta, n_bootstrap, seed), 1.0 - alpha))

    return PairResult(
        gpu_a=str(a.get("gpu", "?")), gpu_b=str(b.get("gpu", "?")),
        n_clips=len(clip_ids), bit_identical_clips=bit_identical,
        abs_delta_mean=round(float(abs_delta.mean()), 8),
        abs_delta_p95=round(float(np.quantile(abs_delta, 0.95)), 8),
        abs_delta_p99=round(float(np.quantile(abs_delta, 0.99)), 8),
        abs_delta_max=round(float(abs_delta.max()), 8),
        mu_hat=round(float(delta.mean()), 8), lcb=v["lcb"], ucb=round(ucb, 8),
    )


def _bootstrap_means(x: np.ndarray, n_bootstrap: int, seed: int) -> np.ndarray:
    rng = np.random.default_rng(seed)
    n = x.shape[0]
    out = np.empty(n_bootstrap, dtype=np.float64)
    for i in range(n_bootstrap):
        out[i] = x[rng.integers(0, n, size=n)].mean()
    return out


def analyze(
    records: Sequence[dict],
    *,
    current_delta_threshold: Optional[float] = None,
    safety_factor: float = DEFAULT_SAFETY_FACTOR,
    alpha: float = 0.001,
    n_bootstrap: int = 10_000,
    seed: int = 0,
) -> CalibrationReport:
    """Turn N per-box records into a cross-GPU noise report + a delta recommendation."""
    clip_ids = _validate_records(records)

    pairs: list[PairResult] = []
    for i in range(len(records)):
        for j in range(i + 1, len(records)):
            pairs.append(_pair(records[i], records[j], clip_ids,
                               alpha=alpha, n_bootstrap=n_bootstrap, seed=seed))

    # The number delta must beat: the worst spurious mean-advantage any pair could show
    # on two models that are actually identical. We take the symmetric bound (max of
    # |lcb|, |ucb|) so a challenger cannot win by noise in either direction.
    max_abs_mu = max((max(abs(p.lcb), abs(p.ucb)) for p in pairs), default=0.0)
    max_abs_delta = max((p.abs_delta_max for p in pairs), default=0.0)
    fully_repro = sum(1 for p in pairs if p.bit_identical_clips == p.n_clips)

    recommended = round(safety_factor * max_abs_mu, 6)

    if current_delta_threshold is None:
        verdict = f"recommend delta_threshold >= {recommended} ({safety_factor}x the measured floor)"
    elif current_delta_threshold >= recommended:
        verdict = (f"PASS: current delta_threshold {current_delta_threshold} clears the measured "
                   f"noise floor (recommended >= {recommended})")
    else:
        verdict = (f"FAIL: current delta_threshold {current_delta_threshold} is BELOW the measured "
                   f"noise floor — two honest validators can fork. Raise it to >= {recommended}")

    gpus = tuple(dict.fromkeys(str(r.get("gpu", "?")) for r in records))
    return CalibrationReport(
        n_boxes=len(records), n_pairs=len(pairs), gpus=gpus,
        max_abs_mu_hat=round(max_abs_mu, 8), max_abs_delta=round(max_abs_delta, 8),
        fully_reproducible_pairs=fully_repro,
        recommended_delta_threshold=recommended,
        current_delta_threshold=current_delta_threshold,
        verdict=verdict, pairs=tuple(pairs),
    )


def box_record(model_ref, *, spec, n_clips: int, gpu_name: str) -> dict:
    """Generate ``model_ref`` on ``n_clips`` calibration clips on THIS box and score each.

    Runs the production generation + scoring path (so the measured noise is the real
    duel's noise), model-vs-truth only — no opponent. Needs a GPU; not unit-tested.
    Produces the record :func:`analyze` consumes.
    """
    from leoma.infra.model_store import ModelRef, materialize_model
    from leoma.infra.storage_backend import create_source_read_client
    from leoma.eval.video_runner import GenParams, load_video_pipeline, generate
    from leoma.eval.dataset import build_duel_clips, fetch_manifest
    from leoma.eval.determinism import apply_determinism, runtime_env
    from leoma.eval.digests import digest_frames
    from leoma.eval.metrics import get_metric
    from leoma.app.validator.seeds import eval_seed, clip_generation_seed

    apply_determinism(spec.determinism)
    ref = model_ref if isinstance(model_ref, ModelRef) else ModelRef(*model_ref)
    snapshot = materialize_model(ref)
    params = GenParams.from_spec(spec.gen)
    pipe = load_video_pipeline(snapshot, gen=spec.gen)

    # A fixed calibration seed — the point is that every box uses the SAME seed, so any
    # distance difference is hardware, not sampling.
    master_seed = eval_seed("calibration", ref.immutable_ref, spec.duel.base_seed)
    client = create_source_read_client()
    manifest = fetch_manifest(client, spec.corpus)
    clips, _ = build_duel_clips(
        manifest, client=client, bucket=spec.corpus.bucket, master_seed=master_seed,
        n_clips=n_clips, gen=params, prompt_mode=spec.gen.prompt_mode, fixed_prompt=spec.gen.prompt,
    )
    distance_fn = get_metric(spec.duel.metric, device=spec.duel.metric_device)

    out_clips = []
    for clip in clips:
        gseed = clip_generation_seed(master_seed, clip.clip_index)
        frames = generate(pipe, clip, gseed)
        out_clips.append({
            "clip_id": clip.clip_id,
            "distance": float(distance_fn(frames, clip.truth_frames)),
            "frames_digest": digest_frames(frames),
        })

    return {
        "gpu": gpu_name,
        "model": ref.immutable_ref,
        "corpus_id": manifest.corpus_id,
        "metric": spec.duel.metric,
        "master_seed": master_seed,
        "env": runtime_env(),
        "clips": out_clips,
    }


